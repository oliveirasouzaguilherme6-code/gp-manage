<?php

session_start();

/*
|--------------------------------------------------------------------------
| LIMPAR DADOS DA SESSÃO
|--------------------------------------------------------------------------
*/

$_SESSION = [];

/*
|--------------------------------------------------------------------------
| REMOVER COOKIE DA SESSÃO
|--------------------------------------------------------------------------
*/

if (ini_get("session.use_cookies")) {

    $params = session_get_cookie_params();

    setcookie(
        session_name(),
        "",
        time() - 42000,
        $params["path"],
        $params["domain"],
        $params["secure"],
        $params["httponly"]
    );
}

/*
|--------------------------------------------------------------------------
| DESTRUIR SESSÃO
|--------------------------------------------------------------------------
*/

session_destroy();

/*
|--------------------------------------------------------------------------
| VOLTAR PARA LOGIN
|--------------------------------------------------------------------------
*/

header("Location: ../login.php");
exit;